package com.Automation;

public class Operator {
    public static void main(String[] args) {
        int a =1;
        System.out.println(a++);
        System.out.println(a++);
        System.out.println(a++);
        System.out.println(a++);
        System.out.println(a++);
        System.out.println(a++);
        System.out.println(a++);
        System.out.println(a++);
        System.out.println(a++);
        System.out.println(a++);

        // Decrement Operator
        System.out.println(" Decrement Operator" );
        System.out.println(a--);
        System.out.println(a--);
        System.out.println(a--);
        System.out.println(a--);
        System.out.println(a--);
        System.out.println(a--);
        System.out.println(a--);
        System.out.println(a--);
        System.out.println(a--);
        System.out.println(a--);
        System.out.println(a--);

    }
}



